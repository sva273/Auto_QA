class SimpleMath:

    """Класс с простыми математическими операциями"""

    def square(self, x):
        """
        Вычисление квадрата числа
        :param x: число
        :return: квадрат числа
        """
        return x * x


    def cube(self, x):
        """
        Вычисление числа в 3-й степени
        :param x: число,
        :return: число в 3-й степени
        """
        return x * x * x